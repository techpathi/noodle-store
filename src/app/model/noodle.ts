export class Noodle {
    Brand: string;
    Variety: string;
    Style: string;
    Country: string;
    Stars: number;
    TopTen: string;
    constructor(Brand: string, Variety: string, Style: string, Country: string, Stars: number, TopTen: string) {
        this.Brand = Brand;
        this.Variety = Variety;
        this.Style = Style;
        this.Country = Country;
        this.Stars = Stars;
        this.TopTen = TopTen;
    }
}
